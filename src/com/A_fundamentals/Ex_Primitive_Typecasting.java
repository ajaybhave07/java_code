package com.A_fundamentals;

public class Ex_Primitive_Typecasting {
	
	public static void main(String[] args) {
		
		//down-casting
		int i = (int) 22.345;
		System.out.println(i);//22
		
		char c = (char)155.67;
		System.out.println(c); //?
		
		byte b = (byte)1223;
		System.out.println(b); //-57
		
		
	
		
		
	}

}
