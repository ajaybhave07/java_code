package com.A_fundamentals;

public class Datatype_Floating_1 {

	public static void main(String[] args) {

//		Floating point literal is by default double type but we can specify explicitly as float type
//		by suffixing with f or F.
		float f1 = 23.65443f;
		float f2 = 34.65689F;
		
		
		
		
//		float f2 = 221.54; C.E:possible loss of precision(invalid)
		
		double d1 = 21.478457;
		
		
	//	We can specify explicitly floating point literal as double type by suffixing with d or D.
		double d=123.456D;
		double d2 = 2135.99878775d;
		
	}

}
